    private void btnupdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnupdateMouseClicked
        if (comid.getText().isEmpty() || comname.getText().isEmpty() || comadd.getText().isEmpty() || comexp.getText().isEmpty() || comphone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "missing information");
        } else {
            try {

                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmadb", "root", "nh123456");
                PreparedStatement pst = con.prepareStatement("update companytb set comname=?,comad=?,comexp=?,comphone=? where comid=?");

                pst.setString(1, comname.getText());
                pst.setString(2, comadd.getText());
                pst.setInt(3, Integer.parseInt(comexp.getText()));
                pst.setString(4, comphone.getText());
                pst.setInt(5, Integer.parseInt(comid.getText()));

                pst.executeUpdate();
                selectmed();
                JOptionPane.showMessageDialog(this, "company updated successfully");
            } catch (Exception e) {
            }

        }
    }//GEN-LAST:event_btnupdateMouseClicked

