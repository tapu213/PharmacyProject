    private void btnaddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnaddMouseClicked
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmadb", "root", "nh123456");
            PreparedStatement pst = con.prepareStatement("insert into companytb values(?,?,?,?,?)");
            pst.setInt(1, Integer.parseInt(comid.getText()));
            pst.setString(2, comname.getText());
            pst.setString(3, comadd.getText());
            pst.setInt(4, Integer.parseInt(comexp.getText()));
            pst.setString(5, comphone.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "company successfully added");
            con.close();
            selectmed();

        } catch (SQLException e) {
        }
    }//GEN-LAST:event_btnaddMouseClicked

    private void btndeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btndeleteMouseClicked
        if (comid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "enter id for delete company");
        } else {
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmadb", "root", "nh123456");
                PreparedStatement pst = con.prepareStatement("delete from companytb where comid=?");
                pst.setInt(1, Integer.parseInt(comid.getText()));
                pst.executeUpdate();
                selectmed();
                JOptionPane.showMessageDialog(this, "company deleted successfully");
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btndeleteMouseClicked

